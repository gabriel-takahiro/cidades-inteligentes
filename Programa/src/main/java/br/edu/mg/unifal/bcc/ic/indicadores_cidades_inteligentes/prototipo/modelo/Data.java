package br.edu.mg.unifal.bcc.ic.indicadores_cidades_inteligentes.prototipo.modelo;

public class Data {

	private String data;
	private String codigoData;

	public Data(String data, String codigoData) {
		this.data = data;
		this.codigoData = codigoData;
	}

	public String getData() {
		return data;
	}

	public String getCodigoData() {
		return codigoData;
	}

}
